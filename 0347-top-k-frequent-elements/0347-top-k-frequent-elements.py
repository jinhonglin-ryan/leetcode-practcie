class Solution(object):
    def topKFrequent(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: List[int]
        """
        
        num_count = {}
        for num in nums:
            if num in num_count:
                num_count[num] += 1
            else:
                num_count[num] = 1
        sorted_items = sorted(num_count.items(), key=lambda item: item[1], reverse=True)
    
        return [item[0] for item in sorted_items[:k]]